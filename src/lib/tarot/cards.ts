export const structuredCards: StructuredCardData[] = cardSpecs.map(createCard)

export const structuredCardMap = structuredCards.reduce<Record<string, StructuredCardData>>(
  (accumulator, card) => {
    accumulator[card.id] = card
    return accumulator
  },
  {},
)

export const structuredCardByNo = structuredCards.reduce<Record<number, StructuredCardData>>(
  (accumulator, card, index) => {
    accumulator[index] = card
    return accumulator
  },
  {},
)

export function getStructuredCard(cardId: string) {
  return structuredCardMap[cardId]
}

export function getStructuredCardByNo(cardNo: number) {
  return structuredCardByNo[cardNo]
}
