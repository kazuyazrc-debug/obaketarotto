import {
  sharedAdvices,
  sharedClosings,
  sharedHidden,
  sharedOpeners,
  sharedStates,
  sharedWarnings,
} from './fragments'
import type {
  FragmentDefinition,
  StructuredCardData,
  Topic,
} from './types'

type CardSpec = {
  id: string
  displayName: string
  coreTheme: string[]
  lightMeaning: string[]
  shadowMeaning: string[]
  emotions: string[]
  warnings: string[]
  actions: Record<Topic, string[]>
  scene: string[]
  omen: string[]
  shift: string[]
  shadowShift: string[]
  comfort: string[]
  release: string[]
}

type SeedCardSpec = Omit<
  CardSpec,
  'scene' | 'omen' | 'shift' | 'shadowShift' | 'comfort' | 'release'
> & {
  scene: string | string[]
  omen: string | string[]
  shift: string | string[]
  shadowShift: string | string[]
  comfort: string | string[]
  release: string | string[]
}

function createFragment(
  id: string,
  kind: FragmentDefinition['kind'],
  text: string,
  options: Partial<Omit<FragmentDefinition, 'id' | 'kind' | 'text'>> = {},
): FragmentDefinition {
  return {
    id,
    kind,
    text,
    bias: options.bias ?? 'neutral',
    tones: options.tones,
    topics: options.topics,
    position: options.position ?? 'any',
    energy: options.energy,
    adviceMode: options.adviceMode,
    keywords: options.keywords,
    tags: options.tags,
  }
}

function actions(values: Record<Topic, string[]>) {
  return values
}

function toVariantArray(value: string | string[]) {
  return Array.isArray(value) ? value : [value]
}

function normalizeCardSpec(spec: SeedCardSpec): CardSpec {
  return {
    ...spec,
    scene: toVariantArray(spec.scene),
    omen: toVariantArray(spec.omen),
    shift: toVariantArray(spec.shift),
    shadowShift: toVariantArray(spec.shadowShift),
    comfort: toVariantArray(spec.comfort),
    release: toVariantArray(spec.release),
  }
}

